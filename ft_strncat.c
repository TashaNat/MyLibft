#include "libft.h"

char	*ft_strncat(char *dest, char *src, size_t nb)
{
	size_t		i;
	size_t		length;

	length = 0;
	while (dest[length] != '\0')
		length++;
	i = 0;
	while (i < nb && src[i] != '\0')
	{
		dest[length + i] = src[i];
		i++;
	}
	dest[length + i] = '\0';
	return (dest);
}
