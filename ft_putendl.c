#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;

	if (!s)
		return ;
	i = 0;
	while (*(const char*)(s + i))
	{
		write(1, &*(const char*)(s + i), 1);
		i++;
	}
	write(1, "\n", 1);
}
